package ch01;

public class _01_VariableEx {
	public static void main(String[] args) {
		/**
		 * A : 아스키 값 65 /  B : 아스키 값 66
		 * */
		char aa = 'A';
		System.out.println(aa);
		System.out.println((int)aa);
		
		System.out.println("-----------------------");
		char bb = 68;
		System.out.println(bb);
		
		System.out.println("-----------------------");
		int cc = 69;
		System.out.println(cc);
		System.out.println((char)cc);
		
	}
}
